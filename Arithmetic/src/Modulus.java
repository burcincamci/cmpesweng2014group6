public class Modulus {
	
	/**
	 * Function for modulus operation.
	 * @param a First operand for modulus.
	 * @param b Second operand for modulus.
	 * @return Result of the modulus.
	 * */
	public int modulus(int a, int b){
		return a%b;
	}
	
}
