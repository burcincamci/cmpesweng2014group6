public class Substract {
	
	/**
	 * Function for substract operation.
	 * @param a First operand for substract.
	 * @param b Second operand for substract.
	 * @return Result of the substract.
	 * */	
	public int substract(int a, int b){
		return a-b;
	}
	
}

