public class Power {
	/**
	 * Function for power operation.
	 * @param a First operand for power.
	 * @param b Second operand for power.
	 * @return Result of the power.
	 * */	
	public  int pow(int a, int b){
		return (int) Math.pow(a, b);
	}
	
}
